#include<iostream>
using namespace std;

int main(){
    // this will print the alphabets in capital letters
    for(int i = 65; i<=90; i++ ){
        cout<<(char) i<<" ";
    }
    cout<<"\n\n";
    // this will print the alphabets in small letters
    for(int k = 97; k<=122; k++ ){
        cout<<(char) k<<" ";
    }
    return 0;
}